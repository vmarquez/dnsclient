package dnsclient
import scalaz.NonEmptyList

object Data {
  //example usage:
  val dns = dnsRequest(1234, DnsString(NonEmptyList("scala", List("org"): _*)))
  
  //trait DnsPacket
  case class DnsString(nel: NonEmptyList[String])  
  case class IPV4(a: Int, b: Int, c: Int, d: Int)
  
  trait MessageType
  object DnsRequest extends MessageType
  object DnsResponse extends MessageType

  case class DnsPacket(transactionId: Int, msgtype: MessageType, question: DnsString, addresses: Vector[ResourceRecord])
  case class ResourceRecord(ttl: Long, ip: IPV4)
 
  def dnsRequest(id: Int, question: DnsString): DnsPacket = 
    DnsPacket(id, DnsRequest, question, Vector())
}

